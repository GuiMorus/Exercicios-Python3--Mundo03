# --Função fazendo o dobro do valor
def dobro(valor):
    return valor * 2


# --Função fazendo a metade do valor
def metade(valor):
    return valor / 2


# --Função aumentando valor em porcentagem
def aumentar(valor, porcent):
    return valor + (valor * porcent / 100)


def diminuir(valor, porcent):
    return valor - (valor * porcent / 100)
